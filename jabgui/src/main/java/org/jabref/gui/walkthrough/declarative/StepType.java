package org.jabref.gui.walkthrough.declarative;

/**
 * Defines the different types of walkthrough steps.
 */
public enum StepType {
    /**
     * Full-screen page that covers the entire application.
     */
    FULL_SCREEN,

    /**
     * Panel that appears on the left side of the interface.
     */
    LEFT_PANEL,

    /**
     * Panel that appears on the right side of the interface.
     */
    RIGHT_PANEL,

    /**
     * Panel that appears at the top of the interface.
     */
    TOP_PANEL,

    /**
     * Panel that appears at the bottom of the interface.
     */
    BOTTOM_PANEL
}
